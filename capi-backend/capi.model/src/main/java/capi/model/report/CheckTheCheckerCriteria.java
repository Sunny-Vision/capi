package capi.model.report;

public class CheckTheCheckerCriteria {

	private String refMonth;

	private Integer noOfCases;

	private String telNo;

	private String faxNo;

	private String signatory;

	private String chineseOfficeAddress;

	private String englishOfficeAddress;

	public String getRefMonth() {
		return refMonth;
	}

	public void setRefMonth(String refMonth) {
		this.refMonth = refMonth;
	}

	public Integer getNoOfCases() {
		return noOfCases;
	}

	public void setNoOfCases(Integer noOfCases) {
		this.noOfCases = noOfCases;
	}

	public String getTelNo() {
		return telNo;
	}

	public void setTelNo(String telNo) {
		this.telNo = telNo;
	}

	public String getFaxNo() {
		return faxNo;
	}

	public void setFaxNo(String faxNo) {
		this.faxNo = faxNo;
	}

	public String getSignatory() {
		return signatory;
	}

	public void setSignatory(String signatory) {
		this.signatory = signatory;
	}

	public String getChineseOfficeAddress() {
		return chineseOfficeAddress;
	}

	public void setChineseOfficeAddress(String chineseOfficeAddress) {
		this.chineseOfficeAddress = chineseOfficeAddress;
	}

	public String getEnglishOfficeAddress() {
		return englishOfficeAddress;
	}

	public void setEnglishOfficeAddress(String englishOfficeAddress) {
		this.englishOfficeAddress = englishOfficeAddress;
	}

}
