package capi.model.masterMaintenance;

public class PointToNoteTableList {
	private Integer id;
	private String effectiveDate;
	private String expiryDate;
	private String createdDate;
	private String createdBy;
	private Long noOfQuotation;
	private Long noOfUnit;
	private Long noOfFirm;
	private Long noOfProduct;
	private String pointToNote;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getEffectiveDate() {
		return effectiveDate;
	}
	public void setEffectiveDate(String effectiveDate) {
		this.effectiveDate = effectiveDate;
	}
	public String getExpiryDate() {
		return expiryDate;
	}
	public void setExpiryDate(String expiryDate) {
		this.expiryDate = expiryDate;
	}
	public String getCreatedDate() {
		return createdDate;
	}
	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}
	public String getCreatedBy() {
		return createdBy;
	}
	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}
	public Long getNoOfQuotation() {
		return noOfQuotation;
	}
	public void setNoOfQuotation(Long noOfQuotation) {
		this.noOfQuotation = noOfQuotation;
	}
	public Long getNoOfUnit() {
		return noOfUnit;
	}
	public void setNoOfUnit(Long noOfUnit) {
		this.noOfUnit = noOfUnit;
	}
	public Long getNoOfFirm() {
		return noOfFirm;
	}
	public void setNoOfFirm(Long noOfFirm) {
		this.noOfFirm = noOfFirm;
	}
	public Long getNoOfProduct() {
		return noOfProduct;
	}
	public void setNoOfProduct(Long noOfProduct) {
		this.noOfProduct = noOfProduct;
	}
	public String getPointToNote() {
		return pointToNote;
	}
	public void setPointToNote(String pointToNote) {
		this.pointToNote = pointToNote;
	}
}
