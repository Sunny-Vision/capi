package capi.model.dataImportExport;

public class ImportRebasingOutletTypeList {
	
	private Integer outletTypeId;
	
	private String code;
	
	private String chineseName;
	
	private String englishName;
	
	private String itemCode;

	public Integer getOutletTypeId() {
		return outletTypeId;
	}

	public void setOutletTypeId(Integer outletTypeId) {
		this.outletTypeId = outletTypeId;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getChineseName() {
		return chineseName;
	}

	public void setChineseName(String chineseName) {
		this.chineseName = chineseName;
	}

	public String getEnglishName() {
		return englishName;
	}

	public void setEnglishName(String englishName) {
		this.englishName = englishName;
	}

	public String getItemCode() {
		return itemCode;
	}

	public void setItemCode(String itemCode) {
		this.itemCode = itemCode;
	}
	
	
}
