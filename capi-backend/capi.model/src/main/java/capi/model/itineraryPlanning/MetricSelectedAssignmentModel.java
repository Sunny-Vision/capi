package capi.model.itineraryPlanning;

public class MetricSelectedAssignmentModel {

	public Long selectedAssignments;
	
	public Long selectedQuotations;
	
	public Long selectedBuildings;

	public Long getSelectedAssignments() {
		return selectedAssignments;
	}

	public void setSelectedAssignments(Long selectedAssignments) {
		this.selectedAssignments = selectedAssignments;
	}

	public Long getSelectedQuotations() {
		return selectedQuotations;
	}

	public void setSelectedQuotations(Long selectedQuotations) {
		this.selectedQuotations = selectedQuotations;
	}

	public Long getSelectedBuildings() {
		return selectedBuildings;
	}

	public void setSelectedBuildings(Long selectedBuildings) {
		this.selectedBuildings = selectedBuildings;
	}
	
}
