package capi.model.batch;

import java.util.Date;

public class NoFieldQuotationResult {

	private Integer quotationId;
	private Date recordDate;
	private Date referenceMonth;
	private Date backTrack1;
	private Date backTrack2;
	private Date backTrack3;
	
	public Integer getQuotationId() {
		return quotationId;
	}
	public void setQuotationId(Integer quotationId) {
		this.quotationId = quotationId;
	}
	public Date getRecordDate() {
		return recordDate;
	}
	public void setRecordDate(Date recordDate) {
		this.recordDate = recordDate;
	}
	public Date getReferenceMonth() {
		return referenceMonth;
	}
	public void setReferenceMonth(Date referenceMonth) {
		this.referenceMonth = referenceMonth;
	}
	public Date getBackTrack1() {
		return backTrack1;
	}
	public void setBackTrack1(Date backTrack1) {
		this.backTrack1 = backTrack1;
	}
	public Date getBackTrack2() {
		return backTrack2;
	}
	public void setBackTrack2(Date backTrack2) {
		this.backTrack2 = backTrack2;
	}
	public Date getBackTrack3() {
		return backTrack3;
	}
	public void setBackTrack3(Date backTrack3) {
		this.backTrack3 = backTrack3;
	}
	
	
}
